// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
fn println_opt<T: Copy + std::fmt::Display>(ans: Option<T>) {
    if let Some(ans) = ans {
        println!("{}", ans);
    } else {
        println!("-1");
    }
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let n = read::<usize>();
    let a = read_mat::<usize>(n);

    let t0 = (0..1 << (n - 1))
        .map(|s| {
            let bs = bitset!(n - 1, s);

            (0..n - 1).fold(((0, 0), a[0][0]), |((i, j), x), idx| {
                if bs[idx] {
                    ((i + 1, j), x ^ a[i + 1][j])
                } else {
                    ((i, j + 1), x ^ a[i][j + 1])
                }
            })
        })
        .fold(FxHashMap::default(), |mut t, ((i, j), x)| {
            *t.entry((i, j))
                .or_insert(FxHashMap::default())
                .entry(x)
                .or_insert(0) += 1;
            t
        });
    let t1 = (0..1 << (n - 1))
        .map(|s| {
            let bs = bitset!(n - 1, s);

            (0..n - 1).fold(((n - 1, n - 1), 0), |((i, j), x), idx| {
                if bs[idx] {
                    ((i - 1, j), x ^ a[i][j])
                } else {
                    ((i, j - 1), x ^ a[i][j])
                }
            })
        })
        .fold(FxHashMap::default(), |mut t, ((i, j), x)| {
            *t.entry((i, j))
                .or_insert(FxHashMap::default())
                .entry(x)
                .or_insert(0) += 1;
            t
        });

    let ans = t0
        .keys()
        .map(|k| {
            t0[&k]
                .iter()
                .map(|(&x, &y)| t1[&k].get(&x).copied().unwrap_or(0) * y)
                .sum::<usize>()
        })
        .sum::<usize>();

    println!("{}", ans);
}
