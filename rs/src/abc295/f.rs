// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    };
    ($($x:expr),+,) => {
        it![$($x),+]
    };
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
fn println_opt<T: std::fmt::Display>(ans: Option<T>) {
    if let Some(ans) = ans {
        println!("{}", ans);
    } else {
        println!("-1");
    }
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let t = read::<usize>();
    let slr = read_vec(t, || read_tuple!(String, usize, usize));

    slr.iter()
        .map(|(s, l, r)| {
            let s = s
                .chars()
                .map(|c| c.to_digit(10).unwrap() as usize)
                .rev()
                .collect::<Vec<_>>();
            let l = *l;
            let r = *r;
            let len = s.len();

            (0..=16 - len)
                .map(|i| {
                    // i + len - 1 ï½ž i
                    let calc = |x: usize| {
                        let mut t = iterate(x, |&s| s / 10)
                            .take_while(|&s| s > 0)
                            .map(|s| s % 10)
                            .collect::<Vec<_>>();
                        t.resize(16, 0);

                        let dp = (0..16).rev().fold([[1, 0], [0, 0]], |prev, j| {
                            let mut next = [[0; 2]; 2];

                            for k in 0..10 {
                                if i <= j && j < i + len && k != s[j - i] {
                                    continue;
                                }
                                for b in 0..2 {
                                    if b == 0 && k > t[j] {
                                        continue;
                                    }

                                    for z in 0..2 {
                                        if z == 0 && k == 0 && i <= j && j < i + len {
                                            continue;
                                        }

                                        let next_b = ((b > 0) || k < t[j]) as usize;
                                        let next_z = ((z > 0) || k > 0) as usize;
                                        next[next_z][next_b] += prev[z][b];
                                    }
                                }
                            }

                            next
                        });

                        dp[0][0] + dp[0][1] + dp[1][0] + dp[1][1]
                    };

                    calc(r) - calc(l - 1)
                })
                .sum::<usize>()
        })
        .for_each(|ans| {
            println!("{}", ans);
        })
}
