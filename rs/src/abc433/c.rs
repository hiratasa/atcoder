fn main() {
    input! {
        s: Digits,
    };

    let ans = s
        .chunk_by(|a, b| a == b)
        .map(|v| (v[0], v.len()))
        .tuple_windows()
        .map(|((x, k), (y, l))| if x + 1 == y { min(k, l) } else { 0 })
        .sum::<usize>();

    println!("{ans}");
}

