#[allow(dead_code)]
struct UndirectedGraph {
    adjs: Vec<Vec<usize>>,
}
#[allow(dead_code)]
impl UndirectedGraph {
    fn from_stdin(n: usize, m: usize) -> UndirectedGraph {
        let mut adjs = vec![vec![]; n];
        for _ in 0..m {
            let (u, v) = read_tuple!(usize, usize);
            adjs[u - 1].push(v - 1);
            adjs[v - 1].push(u - 1);
        }
        UndirectedGraph { adjs }
    }
}

fn main() {
    let n: usize = read();

    let (a, b) = read_tuple!(usize, usize);
    let (a, b) = (a - 1, b - 1);

    let m: usize = read();
    let g = UndirectedGraph::from_stdin(n, m);

    let mut q = VecDeque::new();
    let mut costs = vec![usize::MAX; n];
    let mut nums = vec![0; n];

    const M: usize = 1_000_000_007;

    q.push_back(a);
    costs[a] = 0;
    nums[a] = 1;

    while let Some(v) = q.pop_front() {
        if v == b {
            break;
        }

        for &u in &g.adjs[v] {
            let cost = costs[v] + 1;
            if cost < costs[u] {
                q.push_back(u);
                costs[u] = cost;
                nums[u] = nums[v];
            } else if cost == costs[u] {
                nums[u] = (nums[u] + nums[v]) % M;
            }
        }
    }

    println!("{}", nums[b]);
}
