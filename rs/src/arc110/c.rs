// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

fn main() {
    let n: usize = read();

    let p = read_row::<usize>().into_iter().map(|i| i - 1).collect_vec();

    let ans = p
        .iter()
        .copied()
        .enumerate()
        .try_fold(vec![], |mut ans, (i, pp)| {
            if pp < i {
                ans.extend((pp + 1..=i).rev());
            }
            if ans.len() >= n { None } else { Some(ans) }
        });
    // verify
    if ans.is_none() {
        println!("-1");
        return;
    }
    let ans = ans.unwrap();
    if ans.iter().copied().sorted().dedup().count() != n - 1 {
        println!("-1");
        return;
    }

    let mut q = p.clone();
    for &a in &ans {
        q.swap(a - 1, a);
    }

    if izip!(q.iter().copied(), 0..n).all(|(a1, a2)| a1 == a2) {
        for a in ans {
            println!("{}", a);
        }
    } else {
        println!("-1");
    }
}
