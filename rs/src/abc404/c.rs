fn main() {
    input! {
        n: usize, m: usize,
        ab: [(Usize1, Usize1); m],
    };

    if n != m {
        println!("No");
        return;
    }

    let mut uf = ab.iter().fold(UnionFind::new(n), |mut uf, &(a, b)| {
        uf.unite(a, b);
        uf
    });
    if uf.size(0) != n {
        println!("No");
        return;
    }

    let degs = ab.iter().fold(vec![0; n], |mut degs, &(a, b)| {
        degs[a] += 1;
        degs[b] += 1;
        degs
    });
    if degs.iter().any(|&d| d != 2) {
        println!("No");
        return;
    }

    println!("Yes");
}

