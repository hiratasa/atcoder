fn main() {
    input! {
        n: usize, t: usize, p: usize,
        l: [usize; n],
    };

    let ans = l
        .into_iter()
        .map(|x| t.saturating_sub(x))
        .sorted()
        .nth(p - 1)
        .unwrap();

    println!("{ans}");
}

