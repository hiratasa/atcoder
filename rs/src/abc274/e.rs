// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
fn println_opt<T: Copy + std::fmt::Display>(ans: Option<T>) {
    if let Some(ans) = ans {
        println!("{}", ans);
    } else {
        println!("-1");
    }
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (n, m) = read_tuple!(usize, usize);
    let xy = read_vec(n, || read_tuple!(f64, f64));
    let pq = read_vec(m, || read_tuple!(f64, f64));

    let distance =
        |(x, y): (f64, f64), (x2, y2): (f64, f64)| ((x - x2).powi(2) + (y - y2).powi(2)).sqrt();

    let coord = |i: usize| {
        if i == 0 {
            (0.0, 0.0)
        } else if i - 1 < n {
            xy[i - 1]
        } else {
            pq[i - 1 - n]
        }
    };

    let dp = (0..1 << (n + m + 1)).fold(
        vvec![vvec![0.0; f64::MAX; n + m + 1]; vec![f64::MAX; n + m + 1]; 1<<(n+m+1)],
        |mut dp, s| {
            for i in 0..=n + m {
                let d = dp[s][i];
                if d == f64::MAX {
                    continue;
                }
                let speed = (1usize << ((s >> (n + 1)).count_ones())) as f64;
                for j in 0..=n + m {
                    if s & (1 << j) > 0 {
                        continue;
                    }

                    dp[s ^ (1 << j)][j] = f64::min(
                        dp[s ^ (1 << j)][j],
                        d + distance(coord(i), coord(j)) / speed,
                    );
                }
            }

            dp
        },
    );

    let ans = (0..1 << m)
        .map(|s| (s << (n + 1)) + (1 << (n + 1)) - 1)
        .map(|s| dp[s][0])
        .min_by_key(|&d| ordered_float::OrderedFloat(d))
        .unwrap();

    println!("{}", ans);
}
