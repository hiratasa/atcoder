// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    };
    ($($x:expr),+,) => {
        it![$($x),+]
    };
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
fn println_opt<T: std::fmt::Display>(ans: Option<T>) {
    if let Some(ans) = ans {
        println!("{}", ans);
    } else {
        println!("-1");
    }
}

#[allow(dead_code)]
trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (n, q) = read_tuple!(usize, usize);
    let a = read_row::<usize>();
    let lr = read_vec(q, || read_tuple!(usize, usize));

    let b = max(1, (n as f64 / (q as f64).sqrt().round()) as usize);

    let add = |nums: &mut FxHashMap<usize, usize>, current: usize, x: usize| {
        let c = nums.entry(x).or_insert(0);
        *c += 1;

        current + (*c - 1) * c.saturating_sub(2) / 2
    };

    let remove = |nums: &mut FxHashMap<usize, usize>, current: usize, x: usize| {
        let c = nums.get_mut(&x).unwrap();
        *c -= 1;

        current - *c * c.saturating_sub(1) / 2
    };

    let ans = lr
        .citer()
        .enumerate()
        .sorted_by_key(|&(_, (l, r))| (l / b, r))
        .scan(
            (FxHashMap::default(), 0, 0, 0),
            |(nums, l0, r0, val), (i, (l, r))| {
                let l = l - 1;

                while l < *l0 {
                    *l0 -= 1;
                    *val = add(nums, *val, a[*l0]);
                }

                while *r0 < r {
                    *val = add(nums, *val, a[*r0]);
                    *r0 += 1;
                }

                while *l0 < l {
                    *val = remove(nums, *val, a[*l0]);
                    *l0 += 1;
                }

                while r < *r0 {
                    *r0 -= 1;
                    *val = remove(nums, *val, a[*r0]);
                }

                Some((i, *val))
            },
        )
        .fold(vec![0; q], |mut ans, (i, x)| {
            ans[i] = x;
            ans
        });

    for x in ans {
        println!("{}", x);
    }
}
