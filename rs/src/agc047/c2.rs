fn fft<
    T: Copy
        + std::ops::Add<Output = T>
        + std::ops::Sub<Output = T>
        + std::ops::Mul<Output = T>
        + num::One,
>(
    f: &mut Vec<T>,
    es: &[T],
) {
    let n = f.len();

    if n == 1 {
        return;
    }

    assert!(n.is_power_of_two());

    let d = n.trailing_zeros() as usize;

    for i in 0..n {
        let j = i.reverse_bits() >> (std::mem::size_of::<usize>() * 8 - d);

        if i < j {
            f.swap(i, j);
        }
    }

    for i in 0..d {
        let b = 1 << i;
        let c = n >> (i + 1); // b * c == n/2

        for j in 0..c {
            for k in 0..b {
                let p = es[k * c];
                let t1 = f[j * 2 * b + k];
                let t2 = f[j * 2 * b + k + b];
                f[j * 2 * b + k] = t1 + p * t2;
                f[j * 2 * b + k + b] = t1 - p * t2;
            }
        }
    }
}

use num::complex::Complex64;

#[allow(dead_code)]
fn fft_complex(f: &mut Vec<Complex64>) {
    let n = f.len();
    fft(
        f,
        &(0..n)
            .map(|i| Complex64::from_polar(1.0, 2.0 * i as f64 * std::f64::consts::PI / (n as f64)))
            .collect::<Vec<_>>(),
    );
}

#[allow(dead_code)]
fn inv_fft_complex(f: &mut Vec<Complex64>) {
    let n = f.len();

    fft(
        f,
        &(0..n)
            .map(|i| {
                Complex64::from_polar(1.0, -2.0 * i as f64 * std::f64::consts::PI / (n as f64))
            })
            .collect::<Vec<_>>(),
    );
    for x in f {
        *x /= n as f64;
    }
}

#[allow(dead_code)]
fn convolution<T: Copy + num::ToPrimitive + num::FromPrimitive>(p: &Vec<T>, q: &Vec<T>) -> Vec<T> {
    let n0 = p.len();
    let n1 = q.len();

    let n = (n0 + n1 - 1).next_power_of_two();

    // T => f64 => Complex64
    let mut pf = p
        .iter()
        .map(|x| x.to_f64().unwrap().into())
        .chain(std::iter::repeat(Complex64::new(0.0, 0.0)))
        .take(n)
        .collect::<Vec<_>>();
    let mut qf = q
        .iter()
        .map(|x| x.to_f64().unwrap().into())
        .chain(std::iter::repeat(Complex64::new(0.0, 0.0)))
        .take(n)
        .collect::<Vec<_>>();

    fft_complex(&mut pf);
    fft_complex(&mut qf);

    for (x, y) in pf.iter_mut().zip(&qf) {
        *x *= *y;
    }

    inv_fft_complex(&mut pf);

    pf.iter()
        .map(|x| T::from_f64(x.re.round()).unwrap())
        .take(n0 + n1 - 1)
        .collect()
}

fn main() {
    const M: usize = 200003;

    type Mod = Mod200003;

    let n: usize = read();
    let a = read_row::<usize>();

    const Q: usize = 2;

    let pows = iterate(Mod::one(), |&q| q * Q).take(M - 1).collect_vec();
    let logs = pows
        .citer()
        .enumerate()
        .fold(vec![None; M], |mut logs, (i, pp)| {
            logs[pp.0] = Some(i);
            logs
        });

    let freq = a
        .citer()
        .filter(|&aa| aa > 0)
        .map(|aa| logs[aa].unwrap())
        .fold(vec![0; M - 1], |mut freq, bb| {
            freq[bb] += 1;
            freq
        });
    let conv = convolution(&freq, &freq);
    let sqsum = a.citer().map(|aa| aa * aa % M).sum::<usize>();

    let ans = (conv
        .citer()
        .enumerate()
        .map(|(i, c)| pows[i % (M - 1)].0 * c)
        .sum::<usize>()
        - sqsum)
        / 2;
    println!("{}", ans);
}
