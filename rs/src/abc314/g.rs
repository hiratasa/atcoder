// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    };
    ($($x:expr),+,) => {
        it![$($x),+]
    };
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
fn println_opt<T: std::fmt::Display>(ans: Option<T>) {
    if let Some(ans) = ans {
        println!("{}", ans);
    } else {
        println!("-1");
    }
}

#[allow(dead_code)]
trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (n, m, h) = read_tuple!(usize, usize, usize);
    let ab = read_vec(n, || read_tuple!(usize, usize));

    let mut ans = ab
        .citer()
        .scan(
            (
                0,
                vec![0; m + 1],
                (1..=m).map(|i| (0, i)).collect::<BTreeSet<_>>(),
                once((usize::MAX, usize::MAX)).collect::<BTreeSet<_>>(),
            ),
            |(sum, sums, t, u), (a, b)| {
                let s0 = sums[b];
                sums[b] += a;
                let s = sums[b];

                if t.contains(&(s0, b)) {
                    *sum -= s0;
                    t.remove(&(s0, b));
                }
                if u.contains(&(s0, b)) {
                    u.remove(&(s0, b));
                }

                if *u.first().unwrap() < (s, b) {
                    let (ss, _) = *u.first().unwrap();
                    *sum += ss;
                    let tmp = *u.first().unwrap();
                    t.insert(tmp);
                    u.remove(&tmp);
                    u.insert((s, b));
                } else {
                    t.insert((s, b));
                    *sum += s;
                }

                while *sum >= h {
                    let (ss, _) = *t.last().unwrap();
                    *sum -= ss;
                    let tmp = *t.last().unwrap();
                    u.insert(tmp);
                    t.remove(&tmp);
                }

                Some(u.len() - 1)
            },
        )
        .enumerate()
        .fold(vec![0; m + 1], |mut table, (i, j)| {
            table[j] = i + 1;
            table
        });

    for i in 0..m {
        ans[i + 1] = max(ans[i], ans[i + 1]);
    }

    println!("{}", ans.citer().join(" "));
}
