// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        bs.buffer_mut()[0] = $x as u64;
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let mut c = $c;
        c.push($x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (k, m) = read_tuple!(usize, usize);
    let m_digits = iterate(m, |&mm| mm / 10)
        .take_while(|&mm| mm > 0)
        .map(|mm| mm % 10)
        .collect_vec()
        .into_iter()
        .rev()
        .collect_vec();

    if k <= 100000 {
        let mut dp = vec![vec![0usize; k]; 2];
        dp[0][0] = 1;
        for i in 0..m_digits.len() {
            let e = 10usize.pow((m_digits.len() - i - 1) as u32);

            let mut next = vec![vec![0; k]; 2];

            for lower_m in 0..2 {
                for r in 0..k {
                    for d in 0..10 {
                        if lower_m == 0 && d > m_digits[i] {
                            break;
                        }

                        next[lower_m | (d < m_digits[i]) as usize][(r + d * e + k - d) % k] +=
                            dp[lower_m][r];
                    }
                }
            }

            dp = next;
        }

        let ans = dp[0][0] + dp[1][0] - /* zero */ 1;
        println!("{}", ans);
    } else {
        let ans = (0..)
            .flat_map(|i| i * k..i * k + 100)
            .take_while(|&i| i <= m)
            .filter(|&i| {
                let s = iterate(i, |ii| ii / 10)
                    .take_while(|ii| *ii > 0)
                    .map(|ii| ii % 10)
                    .sum::<usize>();
                i % k == s % k
            })
            .filter(|&i| i > 0)
            .count();
        println!("{}", ans);
    }
}
