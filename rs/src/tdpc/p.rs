mod detail {
    #[allow(dead_code)]
    #[derive(Clone, Copy, Debug)]
    pub struct Edge<W = ()>
    where
        W: Copy,
    {
        pub from: usize,
        pub to: usize,
        pub label: W,
    }
    #[allow(dead_code)]
    impl<W> Edge<W>
    where
        W: Copy,
    {
        pub fn new(from: usize, to: usize) -> Self
        where
            W: Default,
        {
            Self {
                from,
                to,
                label: W::default(),
            }
        }
        pub fn new_with_label(from: usize, to: usize, label: W) -> Self {
            Self { from, to, label }
        }
        pub fn rev(&self) -> Self {
            Self {
                from: self.to,
                to: self.from,
                ..*self
            }
        }
        pub fn offset1(&self) -> Self {
            Self {
                from: self.from - 1,
                to: self.to - 1,
                ..*self
            }
        }
    }
    pub type UnweightedEdge = Edge<()>;
    pub type WeightedEdge = Edge<usize>;
    impl std::convert::From<(usize, usize)> for UnweightedEdge {
        fn from(t: (usize, usize)) -> Self {
            UnweightedEdge::new(t.0, t.1)
        }
    }
    impl std::convert::From<&(usize, usize)> for UnweightedEdge {
        fn from(t: &(usize, usize)) -> Self {
            Edge::from(*t)
        }
    }
    impl std::convert::From<(usize, usize, usize)> for WeightedEdge {
        fn from(t: (usize, usize, usize)) -> Self {
            Edge::new_with_label(t.0, t.1, t.2)
        }
    }
    impl std::convert::From<&(usize, usize, usize)> for WeightedEdge {
        fn from(t: &(usize, usize, usize)) -> Self {
            Edge::from(*t)
        }
    }
    #[allow(dead_code)]
    #[derive(Clone, Debug)]
    pub struct Graph<W = ()>
    where
        W: Copy,
    {
        pub out_edges: Vec<Vec<Edge<W>>>,
        pub in_edges: Vec<Vec<Edge<W>>>,
    }
    #[allow(dead_code)]
    pub type UnweightedGraph = Graph<()>;
    #[allow(dead_code)]
    pub type WeightedGraph = Graph<usize>;
    #[allow(dead_code)]
    impl<W: Copy> Graph<W> {
        pub fn new(n: usize) -> Self {
            Self {
                out_edges: vec![vec![]; n],
                in_edges: vec![vec![]; n],
            }
        }
        pub fn from_edges_directed<T, I>(n: usize, edges: I) -> Self
        where
            I: IntoIterator<Item = T>,
            T: std::convert::Into<Edge<W>>,
        {
            let mut g = Graph::new(n);
            for edge in edges {
                let e = edge.into();
                g.add_edge(e);
            }
            g
        }
        pub fn from_edges1_directed<T, I>(n: usize, edges: I) -> Self
        where
            I: IntoIterator<Item = T>,
            T: std::convert::Into<Edge<W>>,
        {
            Graph::from_edges_directed(n, edges.into_iter().map(|e| e.into()).map(|e| e.offset1()))
        }
        pub fn from_edges_undirected<T, I>(n: usize, edges: I) -> Self
        where
            I: IntoIterator<Item = T>,
            T: std::convert::Into<Edge<W>>,
        {
            Graph::from_edges_directed(
                n,
                edges
                    .into_iter()
                    .map(|e| e.into())
                    .flat_map(|e| std::iter::once(e).chain(std::iter::once(e.rev()))),
            )
        }
        pub fn from_edges1_undirected<T, I>(n: usize, edges: I) -> Self
        where
            I: IntoIterator<Item = T>,
            T: std::convert::Into<Edge<W>>,
        {
            Graph::from_edges1_directed(
                n,
                edges
                    .into_iter()
                    .map(|e| e.into())
                    .flat_map(|e| std::iter::once(e).chain(std::iter::once(e.rev()))),
            )
        }
        pub fn size(&self) -> usize {
            self.out_edges.len()
        }
        pub fn add_edge<T>(&mut self, e: T)
        where
            Edge<W>: std::convert::From<T>,
        {
            let edge = Edge::from(e);
            self.out_edges[edge.from].push(edge);
            self.in_edges[edge.to].push(edge);
        }
        pub fn adjs<'a>(&'a self, v: usize) -> impl 'a + DoubleEndedIterator<Item = usize> {
            self.out_edges[v].iter().map(|e| e.to)
        }
        pub fn children<'a>(
            &'a self,
            v: usize,
            p: usize,
        ) -> impl 'a + DoubleEndedIterator<Item = usize> {
            self.adjs(v).filter(move |&u| u != p)
        }
        pub fn children_edge<'a>(
            &'a self,
            v: usize,
            p: usize,
        ) -> impl 'a + DoubleEndedIterator<Item = Edge<W>> {
            self.out_edges[v].iter().copied().filter(move |e| e.to != p)
        }
    }
}

type Graph = detail::UnweightedGraph;

fn dfs(g: &Graph, v: usize, p: usize, k: usize) -> [Vec<Mod1000000007>; 3] {
    type Mod = Mod1000000007;

    g.children(v, p)
        .map(|u| dfs(g, u, v, k))
        .map(|t| {
            let l = t[0].len();
            [
                (0..l)
                    .map(|i| t.iter().map(|r| r[i]).sum::<Mod>())
                    .chain(once(Mod::zero()))
                    .take(k + 1)
                    .collect::<Vec<_>>(),
                izip!(
                    once(Mod::zero()).chain(t[0].citer()),
                    t[1].citer().chain(once(Mod::zero()))
                )
                .map(|(x, y)| x + y)
                .take(k + 1)
                .collect::<Vec<_>>(),
            ]
        })
        .fold(
            [vec![Mod::one()], vec![Mod::zero()], vec![Mod::zero()]],
            |t0, t1| {
                let l0 = t0[0].len();
                let l1 = t1[0].len();
                let l = min(l0 + l1 - 1, k + 1);

                iproduct!(
                    t0.iter()
                        .enumerate()
                        .flat_map(|(d, r)| r.citer().enumerate().map(move |(i, x)| (d, i, x))),
                    t1.iter()
                        .enumerate()
                        .flat_map(|(d, r)| r.citer().enumerate().map(move |(i, x)| (d, i, x)))
                )
                .fold(
                    [
                        vec![Mod::zero(); l],
                        vec![Mod::zero(); l],
                        vec![Mod::zero(); l],
                    ],
                    |mut t, ((d0, i, x), (d1, j, y))| {
                        let dec = (d0 == 1 && d1 == 1) as usize;
                        if i + j >= dec && d0 + d1 <= 2 {
                            let ii = i + j - dec;
                            if ii < l {
                                t[d0 + d1][ii] = t[d0 + d1][ii] + x * y;
                            }
                        }
                        t
                    },
                )
            },
        )
}

fn main() {
    type Mod = Mod1000000007;

    let (n, k) = read_tuple!(usize, usize);
    let ab = read_vec(n - 1, || read_tuple!(usize, usize));

    let g = Graph::from_edges1_undirected(n, ab);

    let dp = dfs(&g, 0, n, k);

    println!(
        "{}",
        dp.iter()
            .map(|r| r.get(k).copied().unwrap_or(Mod::zero()))
            .sum::<Mod>()
    );
}
