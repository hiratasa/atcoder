trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

#[allow(dead_code)]
fn solve0(k: usize, a: &[usize]) -> Vec<usize> {
    let n = a.len();

    (0..=n)
        .tuple_combinations()
        .map(|(i, j)| (i, j - 1))
        .map(|(i, j)| {
            let mut aa = a.to_vec();
            aa[i..=j].reverse();
            aa
        })
        .sorted()
        .nth(k - 1)
        .unwrap()
}

fn main() {
    let (n, k) = read_tuple!(usize, usize);
    let a = read_row::<usize>();

    let mut k = k;
    let mut a = a;
    let mut b = vec![];
    let mut rest = (1..=n).collect::<Vec<_>>();
    for i in 0..n {
        if k == 0 {
            b.push(a[i]);
            continue;
        }

        let x = a[i];
        let idx = rest.citer().position(|y| y == x).unwrap();

        if k <= idx {
            let y = rest[k - 1];
            let pos = a.citer().position(|z| z == y).unwrap();
            assert!(i <= pos, "{} <= {}; y={}", i, pos, y);
            a[i..=pos].reverse();
            k = 0;
        } else if k - idx <= (n - 1 - i) * (n - i) / 2 + 1 + i {
            k -= idx;
        } else {
            let y = rest[k - 1 - (n - 1 - i) * (n - i) / 2 - i];
            let pos = a.citer().position(|z| z == y).unwrap();
            assert!(i <= pos, "{} <= {}; y={}", i, pos, y);
            a[i..=pos].reverse();
            k = 0;
        }
        b.push(a[i]);
        rest.remove(idx);
    }

    println!("{}", b.citer().join(" "));
}
