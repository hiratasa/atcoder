// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn solve(xy: &[(i64, i64)], ans: &mut Vec<(i64, i64)>) {
    if xy.len() <= 1 {
        return;
    }

    let n = xy.len();

    let x0 = xy[n / 2].0;

    let already = xy
        .citer()
        .filter(|&(x, _)| x == x0)
        .map(|(_, y)| y)
        .collect::<FxHashSet<_>>();

    ans.extend(
        xy.citer()
            .filter(|&(x, _)| x != x0)
            .map(|(_, y)| y)
            .filter(|&y| !already.contains(&y))
            .sorted()
            .dedup()
            .map(|y| (x0, y)),
    );

    let i0 = xy.citer().position(|(x, _)| x >= x0).unwrap();
    let i1 = xy.citer().position(|(x, _)| x > x0).unwrap_or(n);

    solve(&xy[..i0], ans);
    solve(&xy[i1..], ans);
}

fn main() {
    let n = read::<usize>();
    let xy = read_vec(n, || read_tuple!(i64, i64));

    let xy = xy.citer().sorted().collect::<Vec<_>>();

    let mut ans = vec![];
    solve(&xy, &mut ans);

    let m = ans.len();

    assert!(n + m <= 10000);
    assert!(chain(&xy, &ans).sorted().dedup().count() == n + m);
    assert!(
        ans.citer()
            .all(|(x, y)| 1 <= x && x <= 1000 && 1 <= y && y <= 1000)
    );

    println!("{}", ans.len());
    for (x, y) in ans {
        println!("{} {}", x, y);
    }
}
