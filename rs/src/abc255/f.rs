// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn dfs(
    p: &[usize],
    lower: usize,
    upper: usize,
    ans: &mut [(Option<usize>, Option<usize>)],
) -> usize {
    if p.is_empty() {
        return 0;
    }

    let v = p[0];

    if v < lower || v > upper {
        return 0;
    }

    // left
    let x0 = dfs(&p[1..], lower, v - 1, ans);
    if x0 > 0 {
        ans[v].0 = Some(p[1]);
    }

    // right
    let x1 = dfs(&p[1 + x0..], v + 1, upper, ans);
    if x1 > 0 {
        ans[v].1 = Some(p[1 + x0]);
    }

    1 + x0 + x1
}

fn main() {
    let n = read::<usize>();
    let p = read_row::<usize>();
    let q = read_row::<usize>();

    if p[0] != 1 {
        println!("-1");
        return;
    }

    let r = q
        .citer()
        .enumerate()
        .fold(vec![0; n + 1], |mut r, (i, qq)| {
            r[qq] = i + 1;
            r
        });
    let p2 = p.citer().map(|pp| r[pp]).collect::<Vec<_>>();

    let mut ans = vec![(None, None); n + 1];
    if dfs(&p2, 0, usize::MAX, &mut ans) != n {
        println!("-1");
        return;
    }

    for (l, r) in (1..=n).map(|i| ans[r[i]]) {
        println!(
            "{} {}",
            l.map_or(0, |ll| q[ll - 1]),
            r.map_or(0, |rr| q[rr - 1]),
        );
    }
}
