// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

fn main() {
    let (n, m) = read_tuple!(usize, usize);

    let f = read_vec(n, || read::<usize>() - 1);

    const M: usize = 1_000_000_007;

    let cum = f
        .iter()
        .copied()
        .enumerate()
        .fold(
            (vec![1usize], vec![false; m], 0),
            |(mut cum, mut used, mut l), (i, ff)| {
                while l < i && used[ff] {
                    used[f[l]] = false;
                    l += 1;
                }
                used[ff] = true;
                if l > 0 {
                    cum.push((cum[cum.len() - 1] + (M + cum[cum.len() - 1] - cum[l - 1])) % M);
                } else {
                    cum.push(2 * cum[cum.len() - 1] % M);
                }

                (cum, used, l)
            },
        )
        .0;
    let ans = (M + cum[n] - cum[n - 1]) % M;
    println!("{}", ans);
}
