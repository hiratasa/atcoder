// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (h, w) = read_tuple!(usize, usize);
    let b = read_mat::<i64>(h);

    let calc = |h: usize, w: usize, b: &[Vec<i64>]| {
        (0..h)
            .scan(vec![std::i64::MIN; h], |bybottom, i| {
                let z = (i..h)
                    .scan(vec![0; w], |row, j| {
                        izip!(row.iter_mut(), b[j].citer()).for_each(|(x, y)| *x += y);

                        Some((j, row.clone()))
                    })
                    .map(|(j, row)| {
                        let crow = row.citer().cumsum::<i64>().collect::<Vec<_>>();
                        let z = (0..w)
                            .scan(0i64, |mi, k| Some((k, replace(mi, min(*mi, crow[k])))))
                            .map(|(k, mi)| crow[k] - mi)
                            .max()
                            .unwrap();

                        (j, z)
                    })
                    .inspect(|&(j, z)| {
                        bybottom[j] = max(bybottom[j], z);
                    })
                    .map(|(_, z)| z)
                    .max()
                    .unwrap();

                if i == 0 {
                    Some(std::i64::MIN)
                } else {
                    Some(z + bybottom[0..i].citer().max().unwrap())
                }
            })
            .max()
            .unwrap()
    };

    let ans0 = calc(h, w, &b);

    // transpose
    let b = (0..w)
        .map(|i| (0..h).map(|j| b[j][i]).collect::<Vec<_>>())
        .collect::<Vec<_>>();
    let (h, w) = (w, h);
    let ans1 = calc(h, w, &b);

    let ans = max(ans0, ans1);

    println!("{}", ans);
}
