// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn calc(n: usize, idx: usize) -> (usize, usize) {
    (0..n)
        .try_fold(idx, |idx, i| {
            let k = n - 1 - i;
            if idx >= k {
                Ok(idx - k)
            } else {
                Err((i, i + 1 + idx))
            }
        })
        .unwrap_err()
}

fn main() {
    let (n, l, r) = read_tuple!(usize, usize, usize);

    let (i0, j0) = calc(n, l - 1);
    let (i1, j1) = calc(n, r - 1);

    if i0 == i1 {
        let mut v = (1..=n).collect::<Vec<_>>();

        for j in j0..=j1 {
            v.swap(i0, j);
        }

        println!("{}", v.citer().join(" "));
        return;
    }

    let mut ans = vec![];

    for i in 1..=i0 {
        ans.push(i);
    }
    ans.push(n);

    let mut t = vec![(i0 + 2, j0 + 1), (i0 + 1, i0 + 2), (j0 + 1, n)];

    for i in i0 + 1..i1 {
        let (k0, k1) = *t.last().unwrap();
        ans.push(k1 - 1);

        if k0 + 1 == k1 {
            t.pop();
        } else {
            t.last_mut().unwrap().1 -= 1;
        }
    }

    let mut t2 = t.citer().flat_map(|(k0, k1)| k0..k1).collect::<Vec<_>>();
    for j in i1 + 1..=j1 {
        t2.swap(0, j - i1);
    }

    ans.extend(t2);

    println!("{}", ans.citer().join(" "));
}
