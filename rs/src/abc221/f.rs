fn find_max_depth_path(g: &Graph, v: usize, p: usize) -> Vec<usize> {
    g.out_edges[v]
        .iter()
        .map(|e| e.to)
        .filter(|&u| u != p)
        .map(|u| find_max_depth_path(g, u, v))
        .fold(vec![v], |mpath, path| {
            if path.len() + 1 > mpath.len() {
                pushed!(path, v)
            } else {
                mpath
            }
        })
}

fn find_max_depth_num(g: &Graph, v: usize, p: usize, d: usize) -> (usize, usize) {
    g.out_edges[v]
        .iter()
        .map(|e| e.to)
        .filter(|&u| u != p)
        .map(|u| find_max_depth_num(g, u, v, d + 1))
        .fold((d, 1), |(md, mk), (d, k)| {
            if d > md {
                (d, k)
            } else if d == md {
                (d, k + mk)
            } else {
                (md, mk)
            }
        })
}

fn main() {
    type Mod = Mod998244353;

    let n: usize = read();
    let uv = read_vec(n - 1, || read_tuple!(usize, usize));

    let g = Graph::from_edges1_undirected(n, uv);

    let v = find_max_depth_path(&g, 0, n).first().copied().unwrap();
    let p = find_max_depth_path(&g, v, n);

    let d = p.len() - 1;

    if d % 2 == 0 {
        let c = p[d / 2];

        let ks = g.out_edges[c]
            .citer()
            .map(|e| find_max_depth_num(&g, e.to, c, 1))
            .filter(|&(dd, _)| {
                assert!(dd <= d / 2);
                dd == d / 2
            })
            .map(|(_, k)| k)
            .collect::<Vec<_>>();
        assert!(ks.len() >= 2);

        let ans = ks.citer().map(|k| Mod::new(k + 1)).product::<Mod>() - /* empty set */ 1usize - /* size1 sets */ ks.citer().sum::<usize>();
        println!("{}", ans);
    } else {
        let c1 = p[d / 2];
        let c2 = p[d / 2 + 1];

        let (_d1, k1) = find_max_depth_num(&g, c1, c2, 0);
        let (_d2, k2) = find_max_depth_num(&g, c2, c1, 0);

        assert!(_d1 == d / 2);
        assert!(_d2 == d / 2);

        let ans = Mod::new(k1 * k2);
        println!("{}", ans);
    }
}
