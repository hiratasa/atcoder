// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

fn main() {
    let b = read_row::<u32>();
    let c = b
        .iter()
        .copied()
        .enumerate()
        .sorted_by_key(|&(_i, bb)| bb)
        .map(|(i, _bb)| i)
        .collect_vec();

    let n: usize = read();

    let a = read_vec(n, || read_str());

    a.iter()
        .map(|aa| {
            aa.iter()
                .copied()
                .map(|x| {
                    std::char::from_digit(c[x.to_digit(10).unwrap() as usize] as u32, 10).unwrap()
                })
                .collect::<String>()
                .parse::<usize>()
                .unwrap()
        })
        .sorted()
        .map(|aa| {
            aa.to_string()
                .chars()
                .map(|y| {
                    std::char::from_digit(b[y.to_digit(10).unwrap() as usize] as u32, 10).unwrap()
                })
                .collect::<String>()
        })
        .for_each(|aa| println!("{}", aa));
}
