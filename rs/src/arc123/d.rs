#[allow(dead_code)]
fn lower_bound<T, F>(mut begin: T, mut end: T, epsilon: T, f: F) -> T
where
    T: std::marker::Copy
        + std::ops::Add<T, Output = T>
        + std::ops::Sub<T, Output = T>
        + std::ops::Div<T, Output = T>
        + std::cmp::PartialOrd<T>
        + std::convert::TryFrom<i32>,
    F: Fn(T) -> std::cmp::Ordering,
{
    let two = T::try_from(2).ok().unwrap();
    while end - begin >= epsilon {
        let mid = begin + (end - begin) / two;
        match f(mid) {
            std::cmp::Ordering::Less => {
                begin = mid + epsilon;
            }
            _ => {
                end = mid;
            }
        }
    }
    begin
}
#[allow(dead_code)]
fn lower_bound_int<T, F>(begin: T, end: T, f: F) -> T
where
    T: std::marker::Copy
        + std::ops::Add<T, Output = T>
        + std::ops::Sub<T, Output = T>
        + std::ops::Div<T, Output = T>
        + std::cmp::PartialOrd<T>
        + std::convert::TryFrom<i32>,
    F: Fn(T) -> std::cmp::Ordering,
{
    lower_bound(begin, end, T::try_from(1).ok().unwrap(), f)
}

fn main() {
    let n = read::<usize>();
    let a = read_row::<i64>();

    let (b, c): (Vec<_>, Vec<_>) = once((a[0], 0))
        .chain(a[1..].citer().scan((a[0], 0), |(y, z), x| {
            let s = *y + *z;

            if x > s {
                *y += x - s;
                Some((*y, *z))
            } else {
                *z -= s - x;
                Some((*y, *z))
            }
        }))
        .unzip();

    let bs = once(0).chain(b.citer()).cumsum::<i64>().collect::<Vec<_>>();
    let cs = once(0).chain(c.citer()).cumsum::<i64>().collect::<Vec<_>>();

    let k0 = min(-b[n - 1], c[n - 1]);
    let k1 = max(-b[0], 0);

    let calc = |l: i64| {
        let idx0 = b
            .binary_search_by(|&x| (x + l).cmp(&0).then(Ordering::Greater))
            .unwrap_err();
        let idx1 = c
            .binary_search_by(|&x| (x - l).cmp(&0).reverse().then(Ordering::Greater))
            .unwrap_err();

        -(bs[idx0] + l * idx0 as i64)
            + (bs[n] - bs[idx0] + l * (n - idx0) as i64)
            + (cs[idx1] - l * idx1 as i64)
            - (cs[n] - cs[idx1] - l * (n - idx1) as i64)
    };

    let k = lower_bound_int(k0, k1 + 1, |k| {
        if calc(k) < calc(k + 1) {
            Ordering::Greater
        } else {
            Ordering::Less
        }
    });

    let ans = calc(k);

    println!("{}", ans);
}
