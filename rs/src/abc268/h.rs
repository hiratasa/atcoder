// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
fn println_opt<T: Copy + std::fmt::Display>(ans: Option<T>) {
    if let Some(ans) = ans {
        println!("{}", ans);
    } else {
        println!("-1");
    }
}

#[allow(dead_code)]
trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

const B: usize = 12345;

fn main() {
    let s = read_str();
    let n = read::<usize>();
    let t = read_vec(n, || read_str());

    let bases = (0..500000)
        .scan(1usize, |x, _| Some(replace(x, x.wrapping_mul(B))))
        .collect::<Vec<_>>();

    let thash = t
        .iter()
        .map(|word| {
            let h = word
                .citer()
                .rev()
                .enumerate()
                .map(|(i, c)| bases[i].wrapping_mul(c as usize))
                .fold(0usize, |x, y| x.wrapping_add(y));
            (word.len(), h)
        })
        .fold(FxHashMap::default(), |mut thash, (l, h)| {
            thash.entry(l).or_insert(FxHashSet::default()).insert(h);
            thash
        });

    let lens = thash.keys().copied().sorted().collect::<Vec<_>>();

    let shash = once(0)
        .chain(s.citer().scan(0usize, |h, c| {
            *h = h.wrapping_mul(B).wrapping_add(c as usize);
            Some(*h)
        }))
        .collect::<Vec<_>>();

    let (_, ans) = (1..=s.len()).fold((0, 0), |(last_x, num_ops), i| {
        if let Some(_) = lens
            .citer()
            .take_while(|&l| l <= i && l <= i - last_x)
            .find(|&l| {
                thash[&l].contains(&shash[i].wrapping_sub(shash[i - l].wrapping_mul(bases[l])))
            })
        {
            (i, num_ops + 1)
        } else {
            (last_x, num_ops)
        }
    });

    println!("{}", ans);
}
