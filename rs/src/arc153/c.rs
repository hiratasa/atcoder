// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    };
    ($($x:expr),+,) => {
        it![$($x),+]
    };
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_digits() -> Vec<usize> {
    read::<String>()
        .chars()
        .map(|c| c.to_digit(10).unwrap() as usize)
        .collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
fn println_opt<T: std::fmt::Display>(ans: Option<T>) {
    if let Some(ans) = ans {
        println!("{}", ans);
    } else {
        println!("-1");
    }
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let n = read::<usize>();
    let mut a = read_row::<i64>();

    if n == 1 {
        println!("Yes");
        println!("0");
        return;
    }

    if a[n - 1] < 0 {
        a.iter_mut().for_each(|x| *x *= -1);
    }

    let sums = once(0).chain(a.citer()).cumsum::<i64>().collect::<Vec<_>>();
    let b = (0..n).map(|i| sums[n] - sums[i]).collect::<Vec<_>>();

    if b[0] == 0 && b[1..n - 1].citer().all(|x| x >= 0) {
        println!("No");
        return;
    }

    let mut y = vec![0; n];
    for i in 1..n - 1 {
        y[i] = 1;
    }

    const M: i64 = 100000000000;
    if b[0] > 0 {
        y[0] = -((M + b[0] - 1) / b[0]);
    } else if b[0] < 0 {
        y[0] = (M + b[0].abs() - 1) / b[0].abs();
    } else {
        let i0 = (1..n - 1).find(|&i| b[i] < 0).unwrap();
        y[i0] = (M + b[i0].abs() - 1) / b[i0].abs();
    }
    y[n - 1] = izip!(b[..n - 1].citer(), y[..n - 1].citer())
        .map(|(x, y)| -x * y)
        .sum::<i64>();
    assert!(y[1..n].citer().all(|x| x > 0));

    let ans = y.citer().cumsum::<i64>().collect::<Vec<_>>();

    println!("Yes");
    println!("{}", ans.citer().join(" "));

    assert!(ans.citer().all(|x| x.abs() <= 1000000000000));
    assert!(ans.citer().tuple_windows().all(|(x, y)| x < y));
    assert!(
        izip!(ans.citer(), a.citer())
            .map(|(x, y)| x * y)
            .sum::<i64>()
            == 0
    );
}
