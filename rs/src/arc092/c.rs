// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        bs.buffer_mut()[0] = $x as u64;
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let n = read::<usize>();
    let a = read_row::<i64>();

    let has_pos = a.citer().any(|x| x > 0);

    let (ans, required) = if has_pos {
        let ans0 = a.citer().step_by(2).filter(|&x| x > 0).sum::<i64>();
        let ans1 = a.citer().skip(1).step_by(2).filter(|&x| x > 0).sum::<i64>();

        let p = if ans0 > ans1 { 0 } else { 1 };

        (
            max(ans0, ans1),
            (0..n).map(|i| i % 2 == p && a[i] > 0).collect::<Vec<_>>(),
        )
    } else {
        let pos = a.citer().position_max().unwrap();
        let mut required = vec![false; n];
        required[pos] = true;

        (a[pos], required)
    };

    let mut ops = vec![];
    let mut on_left = true;
    let mut i = 0;
    while i < n {
        if required[i] {
            on_left = false;
            i += 1;
        } else if on_left {
            ops.push(1);
            i += 1;
        } else if i + 1 == n {
            ops.push(2);
            i += 1;
        } else if required[i + 1] {
            ops.push(2);
            i += 1;
        } else if i + 2 < n {
            assert!(!required[i + 2]);
            ops.push(3);
            i += 2;
        } else {
            ops.push(3);
            ops.push(2);
            break;
        }
    }

    println!("{}", ans);
    println!("{}", ops.len());
    for op in ops {
        println!("{}", op);
    }
}
