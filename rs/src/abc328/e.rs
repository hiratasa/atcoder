#[derive(Clone, Copy, Debug)]
enum UnionFindNode {
    Root { size: usize },
    Child { parent: usize },
}
struct UnionFind {
    g: Vec<UnionFindNode>,
}
#[allow(dead_code)]
impl UnionFind {
    fn new(n: usize) -> UnionFind {
        use UnionFindNode::*;
        UnionFind {
            g: (0..n).map(|_| Root { size: 1 }).collect(),
        }
    }
    fn root(&mut self, v: usize) -> usize {
        use UnionFindNode::*;
        let p = match self.g[v] {
            Root { size: _ } => return v,
            Child { parent: p } => p,
        };
        let r = self.root(p);
        self.g[v] = Child { parent: r };
        r
    }
    fn unite(&mut self, v: usize, u: usize) -> bool {
        use UnionFindNode::*;
        let rv = self.root(v);
        let ru = self.root(u);
        if rv == ru {
            return false;
        }
        let size_rv = self.size(rv);
        let size_ru = self.size(ru);
        let (rsmall, rlarge) = if size_rv < size_ru {
            (rv, ru)
        } else {
            (ru, rv)
        };
        self.g[rsmall] = Child { parent: rlarge };
        self.g[rlarge] = Root {
            size: size_rv + size_ru,
        };
        true
    }
    fn same(&mut self, v: usize, u: usize) -> bool {
        self.root(v) == self.root(u)
    }
    fn size(&mut self, v: usize) -> usize {
        use UnionFindNode::*;
        let rv = self.root(v);
        match self.g[rv] {
            Root { size } => size,
            Child { parent: _ } => unreachable!(),
        }
    }
}

fn solve(
    g: &Graph,
    edges: &[(usize, usize, usize)],
    k: usize,
    idx: usize,
    selected: usize,
    memo: &mut FxHashMap<usize, usize>,
) -> usize {
    if let Some(&r) = memo.get(&selected) {
        return r;
    }

    let n = g.size();
    if idx == n {
        let (mut uf, cost) = successors(Some((0, selected)), |&(_, t)| {
            if t == 0 {
                None
            } else {
                let i = ((t >> 1) + 1).next_power_of_two().trailing_zeros() as usize;
                Some((i, t & !(1 << i)))
            }
        })
        .skip(1)
        .map(|(i, _)| i)
        .map(|i| edges[i])
        .fold((UnionFind::new(n), 0), |(mut uf, cost), (u, v, c)| {
            uf.unite(u, v);
            (uf, cost + c)
        });

        if uf.size(0) != n {
            return usize::MAX;
        } else {
            return cost % k;
        }
    }

    let r = g.out_edges[idx]
        .citer()
        .filter(|&e| selected & (1 << e.label) == 0)
        .map(|e| solve(g, edges, k, idx + 1, selected | (1 << e.label), memo))
        .min()
        .unwrap_or(usize::MAX);

    memo.insert(selected, r);

    r
}

fn main() {
    input! {
        n: usize, m: usize, k: usize,
        uvw: [(Usize1, Usize1, usize); m],
    };

    let g =
        Graph::from_edges_undirected(n, uvw.citer().enumerate().map(|(i, (u, v, _))| (u, v, i)));

    let ans = solve(&g, &uvw, k, 1, 0, &mut FxHashMap::default());

    println!("{ans}");
}
