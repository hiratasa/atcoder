#[allow(dead_code)]
trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

#[allow(dead_code)]
fn invmod(a: usize, m: usize) -> Option<usize> {
    let (_zero, g, _u, v) =
        std::iter::successors(Some((a as i64, m as i64, 1, 0)), |&(a, b, u, v)| {
            if a == 0 {
                None
            } else {
                Some((b % a, a, -u * (b / a) + v, u))
            }
        })
        .last()
        .unwrap();

    if g == 1 {
        // |v| < m が保障される
        if v < 0 {
            Some((v + m as i64) as usize)
        } else {
            Some(v as usize)
        }
    } else {
        None
    }
}

// 中国剰余定理
fn crt(rm: &[(usize, usize)]) -> usize {
    rm.citer()
        .fold((0usize, 1usize), |(r0, m0), (r1, m1)| {
            let x = invmod(m0, m1).unwrap();

            let l = m0 * m1;

            // オーバーフローに注意
            ((r0 + (l + r1 - r0) % m1 * x % m1 * m0) % l, l)
        })
        .0
}

fn main() {
    let a = read_col::<usize>(29);

    let xs = [16, 27, 25, 7, 11, 13, 17, 19, 23, 29];
    let rm = xs
        .citer()
        .map(|x| {
            let d = x + 1;
            let r = a[d - 2] % x;

            (r, x)
        })
        .collect::<Vec<_>>();
    let ans = crt(&rm);

    if ans > 1000000000000 {
        println!("invalid");
        return;
    }

    if (2..=30)
        .map(|d| {
            iterate(ans, |&x| x / d)
                .take_while(|&x| x > 0)
                .map(|x| x % d)
                .sum::<usize>()
        })
        .eq(a.citer())
    {
        println!("{}", ans);
    } else {
        println!("invalid");
    }
}
