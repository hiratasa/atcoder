// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        if $n > 0 {
            bs.buffer_mut()[0] = $x as u64;
        }
        bs
    }};
}

#[allow(dead_code)]
fn println_opt<T: std::fmt::Display>(ans: Option<T>) {
    if let Some(ans) = ans {
        println!("{}", ans);
    } else {
        println!("-1");
    }
}

use easy_ext::ext;

#[ext(IterCopyExt)]
impl<'a, I, T> I
where
    Self: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

enum Digits {}

impl Readable for Digits {
    type Output = Vec<usize>;
    fn read<R: std::io::BufRead, S: Source<R>>(source: &mut S) -> Vec<usize> {
        source
            .next_token_unwrap()
            .chars()
            .map(|c| c.to_digit(10).unwrap() as usize)
            .collect()
    }
}

fn main() {
    input! {
        n: usize,
        d: [usize; n],
        (l0, c0, k0): (usize, usize, usize),
        (l1, c1, k1): (usize, usize, usize),
    };

    let dp = d.citer().fold(vec![0; k0 + 1], |prev, x| {
        let mut next = vec![usize::MAX; k0 + 1];
        (0..=k0).for_each(|i| {
            (0..=k0 - i).for_each(|j| {
                next[i + j] = min(
                    next[i + j],
                    prev[i] + (x.saturating_sub(j * l0) + l1 - 1) / l1,
                );
            });
        });
        next
    });

    let ans = dp
        .citer()
        .enumerate()
        .filter_map(|(i, j)| if j <= k1 { Some(i * c0 + j * c1) } else { None })
        .min();

    println_opt(ans);
}
