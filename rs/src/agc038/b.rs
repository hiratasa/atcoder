// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        bs.buffer_mut()[0] = $x as u64;
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let mut c = $c;
        c.push($x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (n, k) = read_tuple!(usize, usize);
    let p = read_row::<usize>();

    let sorted_from = p
        .citer()
        .enumerate()
        .scan((0, 0), |(prev, i_prev), (i, pp)| {
            if *prev > pp {
                *i_prev = i;
            }
            *prev = pp;
            Some(*i_prev)
        })
        .collect::<Vec<_>>();

    let ans = 1 + izip!(p.citer(), p[k - 1..].citer(), p[k..].citer().chain(once(0)))
        .scan(
            p[..k].citer().collect::<BTreeSet<_>>(),
            |s, (p0, p1, p2)| {
                let mi = s.citer().next().unwrap();
                let ma = s.citer().rev().next().unwrap();

                s.remove(&p0);
                s.insert(p2);

                Some((mi == p0, ma == p1))
            },
        )
        .zip(
            sorted_from
                .citer()
                .enumerate()
                .skip(k - 1)
                .map(|(i, i0)| i + 1 - i0 >= k),
        )
        .tuple_windows()
        .filter(|&(((f0, _f1), _), ((_g0, g1), _))| !(f0 && g1))
        .scan(false, |q, ((_, s0), (_, s1))| {
            *q = *q || s0;
            if *q && s1 {
                Some(None)
            } else {
                *q = *q || s1;
                Some(Some(()))
            }
        })
        .flatten()
        .count();

    println!("{}", ans);
}
