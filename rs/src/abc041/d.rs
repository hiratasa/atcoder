// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        bs.buffer_mut()[0] = $x as u64;
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let mut c = $c;
        c.push($x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (n, m) = read_tuple!(usize, usize);
    let xy = read_vec(m, || read_tuple!(usize, usize))
        .into_iter()
        .map(|(x, y)| (x - 1, y - 1))
        .collect_vec();

    let reachable0 = xy.citer().chain((0..n).map(|i| (i, i))).fold(
        vec![vec![false; n]; n],
        |mut reachable, (x, y)| {
            reachable[x][y] = true;
            reachable
        },
    );
    let reachable = iproduct!(0..n, 0..n, 0..n).fold(reachable0, |mut reachable, (k, i, j)| {
        reachable[i][j] |= reachable[i][k] & reachable[k][j];
        reachable
    });

    let ans = iproduct!(0..1 << n, 0..n)
        .map(|(s, i)| (s, bitset!(n, s), i))
        .filter(|(_s, bs, i)| {
            bs[*i]
                && (0..n)
                    .filter(|&j| bs[j] && j != *i)
                    .all(|j| !reachable[*i][j])
        })
        .fold(vec![vec![0; n]; 1 << n], |mut dp, (s, bs, i)| {
            dp[s][i] = if bs.count_ones() == 1 {
                1
            } else {
                (0..n)
                    .filter(|&j| bs[j] && j != i)
                    .map(|j| dp[s ^ (1 << i)][j])
                    .sum::<usize>()
            };
            dp
        })[(1 << n) - 1]
        .citer()
        .sum::<usize>();
    println!("{}", ans);
}
