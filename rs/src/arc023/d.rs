// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let mut c = $c;
        c.push($x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn gcd(x: u32, y: u32) -> u32 {
    if x == 0 { y } else { gcd(y % x, x) }
}

use proconio::input;

fn main() {
    input! {
        n: usize,
        m: usize,
        a: [u32; n],
        x: [u32; m],
    }

    let (t, _) = a
        .citer()
        .fold((FxHashMap::default(), vec![]), |(mut map, mut s), aa| {
            s.push((aa, 1usize));

            s[0] = (gcd(s[0].0, aa), s[0].1);
            *map.entry(s[0].0).or_insert(0) += s[0].1;
            let mut i = 0;
            let mut j = 1;
            while j < s.len() {
                let g = gcd(s[j].0, aa);
                let k = s[j].1;

                if g == s[i].0 {
                    s[i].1 += k;
                } else {
                    i += 1;
                    s[i] = (g, k);
                }
                *map.entry(g).or_insert(0) += k;
                j += 1;
            }
            s.resize_with(i + 1, || unreachable!());

            (map, s)
        });

    let stdout = io::stdout();
    let mut stdout = BufWriter::new(stdout.lock());
    x.citer()
        .map(|xx| t.get(&xx).copied().unwrap_or(0))
        .for_each(|ans| {
            writeln!(stdout, "{}", ans).unwrap();
        });
}
