fn calc(
    d: usize,
    k: usize,
    x: usize,
    from_root: bool,
    memo: &mut FxHashMap<(usize, usize, usize, bool), Option<usize>>,
) -> Option<usize> {
    assert!(x > 0);

    if let Some(&r) = memo.get(&(d, k, x, from_root)) {
        r
    } else {
        let r = if d == 0 {
            if x <= 1 && from_root { Some(0) } else { None }
        } else if from_root && (k.pow(d as u32 + 1) - 1) / (k - 1) == x {
            Some(0)
        } else {
            let r0 = if from_root {
                None
            } else {
                calc(d - 1, k, x, false, memo)
            };
            let r1 = if from_root {
                None
            } else {
                calc(d - 1, k, x, true, memo).map(|r| r + 1)
            };
            let r2 = if x == 1 {
                if from_root { Some(2) } else { Some(1) }
            } else if from_root {
                calc(d - 1, k, x - 1, true, memo).map(|r| r + 1)
            } else {
                None
            };

            let r01 = match r0 {
                Some(r0) => match r1 {
                    Some(r1) => Some(min(r0, r1)),
                    None => Some(r0),
                },
                None => r1,
            };
            match r01 {
                Some(r01) => match r2 {
                    Some(r2) => Some(min(r01, r2)),
                    None => Some(r01),
                },
                None => r2,
            }
        };

        memo.insert((d, k, x, from_root), r);

        r
    }
}

