// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let mut c = $c;
        c.push($x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (r, c, n) = read_tuple!(usize, usize, usize);
    let xy = read_vec(n, || read_tuple!(usize, usize, usize, usize));

    let on_edge = |x: usize, y: usize| (x == 0 || x == r) || (y == 0 || y == c);

    let to_idx = |x: usize, y: usize| {
        assert!(on_edge(x, y));

        if y == 0 {
            x
        } else if x == r {
            r + y
        } else if y == c {
            r + c + (r - x)
        } else {
            assert!(x == 0);
            r + c + r + (c - y)
        }
    };

    let ans = xy
        .citer()
        .filter(|&(x0, y0, x1, y1)| on_edge(x0, y0) && on_edge(x1, y1))
        .enumerate()
        .flat_map(|(i, (x0, y0, x1, y1))| it![(to_idx(x0, y0), i), (to_idx(x1, y1), i)])
        .sorted()
        // .inspect(|x| eprintln!("{:?}", x))
        .try_fold((vec![], vec![false; n]), |(mut st, mut used), (_, i)| {
            if matches!(st.last(), Some(&j) if i == j) {
                st.pop();
                used[i] = false;
            } else if used[i] {
                return None;
            } else {
                st.push(i);
                used[i] = true;
            }

            Some((st, used))
        })
        .is_some();
    if ans {
        println!("YES");
    } else {
        println!("NO");
    }
}
