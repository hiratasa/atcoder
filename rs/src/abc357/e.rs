fn main() {
    input! {
        n: usize,
        a: [Usize1; n],
    };

    let g = Graph::from_edges_directed(n, a.into_iter().enumerate());

    let mut t = vec![(n, 0); n];
    let mut visited = vec![false; n];
    let mut visiting = vec![false; n];
    for i in 0..n {
        calc(&g, i, &mut visiting, &mut visited, &mut t);
    }

    let mut ans = 0;
    for i in 0..n {
        if t[i].0 == n {
            ans += t[i].1;
        } else {
            ans += t[t[i].0].1 + t[i].1;
        }
    }

    println!("{ans}");
}

type Graph = detail::UnweightedGraph;

fn calc(
    g: &Graph,
    v: usize,
    visiting: &mut [bool],
    visited: &mut [bool],
    t: &mut [(usize, usize)],
) -> (usize, usize) {
    let n = g.size();

    if visiting[v] {
        return (v, 0);
    }

    if visited[v] {
        if t[v].0 == n {
            return (n, t[v].1);
        } else {
            return (n, t[t[v].0].1 + t[v].1);
        }
    }

    visiting[v] = true;

    let u = g.out_edges[v][0].to;
    let (s, c) = calc(g, u, visiting, visited, t);

    visiting[v] = false;
    visited[v] = true;

    if s == n || s == v {
        t[v] = (n, c + 1);
        return (n, c + 1);
    } else {
        t[v] = (s, 0);
        return (s, c + 1);
    }
}

