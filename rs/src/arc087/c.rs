// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        bs.buffer_mut()[0] = $x as u64;
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

#[derive(Debug, Clone, Copy)]
struct Trie {
    depth: usize,
    children: [Option<usize>; 2],
}

impl Trie {
    fn new(depth: usize) -> Trie {
        Trie {
            depth,
            children: [None, None],
        }
    }

    fn insert(buf: &mut Vec<Trie>, idx: usize, s: &[char]) {
        if s.is_empty() {
            return;
        }

        let c = (s[0] == '1') as usize;
        if buf[idx].children[c].is_none() {
            buf[idx].children[c] = Some(buf.len());
            buf.push(Trie::new(buf[idx].depth + 1));
        }

        Trie::insert(buf, buf[idx].children[c].unwrap(), &s[1..]);
    }
}

fn main() {
    let (n, l) = read_tuple!(usize, usize);
    let s = read_vec(n, || read_str());

    let mut buf = Vec::with_capacity(100010);
    buf.push(Trie::new(0));

    for ss in &s {
        Trie::insert(&mut buf, 0, ss);
    }

    let grundy = |i: usize| {
        assert!(i < l);

        1 << (l - i).trailing_zeros()
    };

    let g = buf
        .citer()
        .filter(|t| t.children.citer().flatten().count() == 1)
        .map(|t| grundy(t.depth))
        .fold(0, |g, x| g ^ x);

    if g == 0 {
        println!("Bob")
    } else {
        println!("Alice")
    }
}
