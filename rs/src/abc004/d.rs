// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let mut c = $c;
        c.push($x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (r, g, b) = read_tuple!(i64, i64, i64);

    let ans = (min(100 - b - g + 1, -g + 1)..=max(-100 + r, 0))
        .map(|lg| {
            // RED: [-100-r/2, -100+(r-1)/2] or [lg-r, lg-1]
            let nr = if -100 + (r - 1) / 2 < lg {
                (-100 - r / 2..=-100 + (r - 1) / 2)
                    .map(|i| (i + 100).abs())
                    .sum::<i64>()
            } else {
                (lg - r..=lg - 1).map(|i| (i + 100).abs()).sum::<i64>()
            };

            // GREEN: [lg, lg+g-1]
            let ng = (lg..=lg + g - 1).map(|i| i.abs()).sum::<i64>();

            // BLUE: [100-(b-1)/2, 100+b/2] or [lg+g, lg+g+b-1]
            let nb = if 100 - (b - 1) / 2 >= lg + g {
                (100 - (b - 1) / 2..=100 + b / 2)
                    .map(|i| (i - 100).abs())
                    .sum::<i64>()
            } else {
                (lg + g..=lg + g + b - 1)
                    .map(|i| (i - 100).abs())
                    .sum::<i64>()
            };

            nr + ng + nb
        })
        .min()
        .unwrap();

    println!("{}", ans);
}
