// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        bs.buffer_mut()[0] = $x as u64;
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let mut c = $c;
        c.push($x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (k, n) = read_tuple!(usize, usize);

    let vw = read_vec(n, || read_tuple!(String, String));
    let vw = vw
        .iter()
        .map(|(v, w)| {
            (
                v.chars()
                    .map(|c| c.to_digit(10).unwrap() as usize)
                    .map(|d| d - 1)
                    .collect_vec(),
                w,
            )
        })
        .collect_vec();

    let ans = (0..k)
        .map(|_| 1..=3)
        .multi_cartesian_product()
        .find_map(|lens| {
            let words: Vec<_> = vw
                .iter()
                .map(|(v, w)| {
                    let l0 = v.citer().map(|d| lens[d]).sum::<usize>();
                    let l1 = w.len();
                    if l0 != l1 {
                        return None;
                    }

                    Some(
                        v.citer()
                            .scan(0usize, |pos, d| {
                                let t = w.get(*pos..*pos + lens[d]).unwrap();
                                *pos += lens[d];
                                Some((d, t))
                            })
                            .collect_vec(),
                    )
                })
                .collect::<Option<Vec<_>>>()?;
            words
                .into_iter()
                .flatten()
                .try_fold(vec![None; k], |mut ans, (d, u)| {
                    if let Some(u2) = ans[d] {
                        if u2 != u {
                            return None;
                        }
                    }
                    ans[d] = Some(u);
                    Some(ans)
                })
        })
        .unwrap();
    for ans in ans {
        println!("{}", ans.unwrap());
    }
}
