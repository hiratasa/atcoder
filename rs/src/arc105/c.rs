// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        bs.buffer_mut()[0] = $x as u64;
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let x = $x;
        let mut c = $c;
        c.push(x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

#[allow(dead_code)]
trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (n, m) = read_tuple!(usize, usize);
    let w = read_row::<usize>();
    let lv = read_vec(m, || read_tuple!(usize, usize));

    let wmax = w.citer().max().unwrap();
    let vmin = lv.citer().map(|(_, v)| v).min().unwrap();

    if wmax > vmin {
        println!("-1");
        return;
    }

    let t = once((0, 0))
        .chain(lv.citer().sorted_by_key(|&(_, v)| v))
        .scan(0, |lmax, (l, v)| {
            *lmax = max(l, *lmax);
            Some((v, *lmax))
        })
        .collect::<Vec<_>>();

    let ans = (0..n)
        .permutations(n)
        .map(|perm| {
            let s = once(0)
                .chain(perm.citer().map(|i| w[i]))
                .cumsum::<usize>()
                .collect::<Vec<_>>();

            let pos = (0..n).fold(vec![0; n], |mut pos, i| {
                for j in i + 1..n {
                    let ww = s[j + 1] - s[i];

                    let idx = t
                        .binary_search_by(|&(v, _l)| v.cmp(&ww).then(Ordering::Greater))
                        .unwrap_err();
                    assert!(idx > 0);

                    pos[j] = max(pos[j], pos[i] + t[idx - 1].1);
                }
                pos
            });

            pos[n - 1]
        })
        .min()
        .unwrap();

    println!("{}", ans);
}
