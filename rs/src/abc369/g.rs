fn main() {
    input! {
        n: usize,
        uvl: [(Usize1, Usize1, i64); n - 1],
    };

    let g = Graph::<i64>::from_edges_undirected(n, uvl);

    let mut t = vec![];
    let mut idxs = vec![[0; 2]; n];
    dfs(&g, 0, n, &mut t, &mut idxs, &mut 0);

    let mut st = LazySegmentTree::<Max, Sum>::with(&vec![0; 2 * n]);
    for (i, &(_, _, x)) in t.iter().enumerate() {
        st.update(i + 1.., x);
    }

    let mut k = n - 1;
    let mut ans = vec![];
    let mut s = 0;
    let mut used = vec![false; n];
    while k > 0 {
        let z = st.query(0..).0;
        s += z;
        ans.push(2 * s);

        let mut idx = st.right_partition_point(0, |&x| x.0 < z).unwrap();
        idx -= 1;
        while idx > 0 {
            let (p, v, x) = t[idx - 1];
            if used[v] {
                break;
            }
            used[v] = true;
            st.update(idx.., -x);
            st.update(idxs[v][1].., x);

            idx = idxs[p][0];
            k -= 1;
        }
    }

    ans.resize(n, 2 * s);

    for x in ans {
        println!("{x}");
    }
}

fn dfs(
    g: &Graph<i64>,
    v: usize,
    p: usize,
    t: &mut Vec<(usize, usize, i64)>,
    idxs: &mut [[usize; 2]],
    idx: &mut usize,
) {
    idxs[v][0] = *idx;
    *idx += 1;
    g.children_edge(v, p).for_each(|e| {
        t.push((v, e.to, e.label));
        dfs(g, e.to, v, t, idxs, idx);
        t.push((e.to, v, -e.label));
    });
    idxs[v][1] = *idx;
    *idx += 1;
}

