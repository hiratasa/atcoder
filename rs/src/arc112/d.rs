// 強連結成分分解
mod scc {
    use super::*;

    #[allow(dead_code)]
    fn dfs(g: &Graph, v: usize, visited: &mut Vec<bool>, vs: &mut Vec<usize>) {
        visited[v] = true;

        for edge in &g.out_edges[v] {
            if !visited[edge.to] {
                dfs(g, edge.to, visited, vs);
            }
        }

        vs.push(v);
    }

    #[allow(dead_code)]
    fn rev_dfs(g: &Graph, v: usize, visited: &mut Vec<bool>, vs: &mut Vec<usize>) {
        visited[v] = true;
        vs.push(v);

        for edge in &g.in_edges[v] {
            if !visited[edge.from] {
                rev_dfs(g, edge.from, visited, vs);
            }
        }
    }

    #[allow(dead_code)]
    pub fn scc(g: &Graph) -> Vec<Vec<usize>> {
        let mut vs = vec![];
        {
            let mut visited = vec![false; g.size()];
            for v in 0..g.size() {
                if !visited[v] {
                    dfs(g, v, &mut visited, &mut vs);
                }
            }
        }

        let mut ret = vec![];
        {
            let mut visited = vec![false; g.size()];
            for &v in vs.iter().rev() {
                if !visited[v] {
                    let mut component = vec![];
                    rev_dfs(g, v, &mut visited, &mut component);
                    ret.push(component);
                }
            }
        }

        ret
    }
}

fn main() {
    let (h, w) = read_tuple!(usize, usize);
    let s = read_vec(h, || read_str());

    let row_vert = |i: usize| i;
    let col_vert = |i: usize| h + i;

    let g = Graph::from_edges_directed(
        h + w,
        chain(
            s.iter()
                .enumerate()
                .flat_map(|(i, row)| row.citer().positions(|c| c == '#').map(move |j| (i, j)))
                .flat_map(|(i, j)| it![(row_vert(i), col_vert(j)), (col_vert(j), row_vert(i))]),
            chain(
                (0..h)
                    .flat_map(|i| it![(row_vert(i), col_vert(0)), (row_vert(i), col_vert(w - 1))]),
                (0..w)
                    .flat_map(|i| it![(col_vert(i), row_vert(0)), (col_vert(i), row_vert(h - 1))]),
            ),
        ),
    );

    let t = scc::scc(&g);

    let idxs = t
        .iter()
        .enumerate()
        .fold(vec![0; h + w], |mut idxs, (i, c)| {
            c.citer().for_each(|v| idxs[v] = i);
            idxs
        });

    let ans = it![false, true]
        .map(|a| {
            t.iter()
                .enumerate()
                .scan(vec![false; t.len()], |visited, (i, c)| {
                    let x = c.citer().any(|v| (v < h) == a) && !visited[i];

                    visited[i] |= x;

                    if visited[i] {
                        c.citer()
                            .flat_map(|v| g.adjs(v))
                            .map(|u| idxs[u])
                            .for_each(|j| visited[j] = true);
                    }

                    if c.contains(&row_vert(0)) {
                        None
                    } else {
                        Some(x as usize)
                    }
                })
                .sum::<usize>()
        })
        .min()
        .unwrap();

    println!("{}", ans);
}
