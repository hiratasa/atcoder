// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

fn main() {
    let n: usize = read();
    let t = read_row::<usize>();
    let a = read_row::<usize>();

    const M: usize = 1_000_000_007;

    let it_t = t
        .iter()
        .copied()
        .zip(std::iter::once(0).chain(t.iter().copied()))
        .map(|(tt, prev)| if tt > prev { (tt, tt) } else { (1, tt) });
    let it_a = a
        .iter()
        .copied()
        .zip(a.iter().copied().skip(1).chain(std::iter::once(0)))
        .map(|(aa, next)| if aa > next { (aa, aa) } else { (1, aa) });

    let ans = it_t.zip(it_a).fold(1usize, |ans, ((tl, tr), (al, ar))| {
        if tr < al || ar < tl {
            0
        } else {
            ans * (min(tr, ar) - max(tl, al) + 1) % M
        }
    });

    println!("{}", ans);
}
