#[allow(unused_macros)]
macro_rules! read_cols {
    ($($t:ty),+) => {{
        let mut line = String::new();
        stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_vec<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

fn main() {
    let t: usize = read();

    for _ in 0..t {
        let n: usize = read();
        let a = read_vec::<usize>();

        if n % 2 > 0 {
            println!("Second");
        } else {
            if a.iter().fold(BTreeMap::new(), |mut nums, &aa| {
                *nums.entry(aa).or_insert(0) += 1;
                nums
            }).values().any(|num| num % 2 > 0) {
                println!("First");
            } else {
                println!("Second");
            }
        }
    }
}
