#[allow(unused_macros)]
macro_rules! read_cols {
    ($($t:ty),+) => {{
        let mut line = String::new();
        stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_vec<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

const M: i64 = 1000000007;

fn mpow(x: i64, n: usize) -> i64 {
    if n == 0 {
        return 1;
    } else if n == 1 {
        return x;
    }

    let q = mpow(x, n / 2);

    q * q % M * mpow(x, n % 2) % M
}

fn main() {
    let n: usize = read();

    let ans = (mpow(10, n) + 2 * (M - mpow(9, n)) + mpow(8, n)) % M;
    println!("{}", ans);
}
