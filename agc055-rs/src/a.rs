// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        bs.buffer_mut()[0] = $x as u64;
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let mut c = $c;
        c.push($x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let n: usize = read();
    let s = read_str();

    let map = it![
        ([0, 1, 2], 1),
        ([0, 2, 1], 2),
        ([1, 0, 2], 3),
        ([1, 2, 0], 4),
        ([2, 0, 1], 5),
        ([2, 1, 0], 6)
    ]
    .collect::<FxHashMap<_, _>>();

    let mut q = (0..3)
        .map(|i| {
            s[n * i..n * (i + 1)]
                .citer()
                .enumerate()
                .fold(vec![vec![]; 3], |mut q, (j, c)| {
                    q[c as usize - 'A' as usize].push(n * i + j);
                    q
                })
        })
        .collect::<Vec<_>>();

    let mut ans = vec![0; 3 * n];
    for i in 0..n {
        let c0 = (0..3).find(|&c| !q[0][c].is_empty()).unwrap();

        let c1 = (0..3)
            .find(|&c| c != c0 && !q[1][c].is_empty() && !q[2][3 - c0 - c].is_empty())
            .unwrap();

        let c2 = 3 - c0 - c1;

        let pos0 = q[0][c0].pop().unwrap();
        let pos1 = q[1][c1].pop().unwrap();
        let pos2 = q[2][c2].pop().unwrap();

        let idx = *map.get(&[c0, c1, c2]).unwrap();
        ans[pos0] = idx;
        ans[pos1] = idx;
        ans[pos2] = idx;
    }

    println!("{}", ans.citer().join(""));
}
