// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        bs.buffer_mut()[0] = $x as u64;
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let mut c = $c;
        c.push($x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn main() {
    let (n, m) = read_tuple!(usize, usize);
    let s = read_row::<usize>();
    let t = read_row::<usize>();

    let s_has_0 = s.citer().any(|c| c == 0);
    let s_has_1 = s.citer().any(|c| c == 1);
    let t_has_0 = t.citer().any(|c| c == 0);
    let t_has_1 = t.citer().any(|c| c == 1);

    if t_has_0 && !s_has_0 {
        println!("-1");
        return;
    }

    if t_has_1 && !s_has_1 {
        println!("-1");
        return;
    }

    if !s_has_0 || !s_has_1 {
        println!("{}", t.len());
        return;
    }

    let nearest_diff = (1..).find(|&i| s[i] != s[0] || s[n - i] != s[0]).unwrap();

    if !t_has_0 {
        // T is all 1
        if s[0] == 1 {
            println!("{}", t.len());
            return;
        }

        println!("{}", t.len() + nearest_diff);
    } else if !t_has_1 {
        // T is all 0
        if s[0] == 0 {
            println!("{}", t.len());
            return;
        }

        println!("{}", t.len() + nearest_diff);
    } else {
        let k = t.citer().tuple_windows().filter(|&(x, y)| x != y).count();
        if t[0] == s[0] {
            println!("{}", t.len() + nearest_diff + (k - 1));
        } else {
            println!("{}", t.len() + nearest_diff + k);
        }
    }
}
