// vec with some initial value
#[allow(unused_macros)]
macro_rules! vvec {
    ($($x:expr),+; $y:expr; $n:expr) => {{
        let mut v = vec![$y; $n];

        let mut it = v.iter_mut();
        $(
            *it.next().unwrap() = $x;
        )+

        v
    }}
}

#[allow(unused_macros)]
macro_rules! it {
    ($x:expr) => {
        once($x)
    };
    ($first:expr,$($x:expr),+) => {
        chain(
            once($first),
            it!($($x),+)
        )
    }
}

#[allow(unused_macros)]
macro_rules! bitset {
    ($n:expr, $x:expr) => {{
        let mut bs = BitSet::new($n);
        bs.buffer_mut()[0] = $x as u64;
        bs
    }};
}

#[allow(unused_macros)]
macro_rules! pushed {
    ($c:expr, $x:expr) => {{
        let mut c = $c;
        c.push($x);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! inserted {
    ($c:expr, $($x:expr),*) => {{
        let mut c = $c;
        c.insert($($x),*);
        c
    }};
}

#[allow(unused_macros)]
macro_rules! read_tuple {
    ($($t:ty),+) => {{
        let mut line = String::new();
        io::stdin().read_line(&mut line).unwrap();

        let mut it = line.trim()
            .split_whitespace();

        ($(
            it.next().unwrap().parse::<$t>().ok().unwrap()
        ),+)
    }}
}

#[allow(dead_code)]
fn read<T: FromStr>() -> T {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();
    line.trim().to_string().parse().ok().unwrap()
}

#[allow(dead_code)]
fn read_str() -> Vec<char> {
    read::<String>().chars().collect()
}

#[allow(dead_code)]
fn read_row<T: FromStr>() -> Vec<T> {
    let mut line = String::new();
    io::stdin().read_line(&mut line).unwrap();

    line.trim()
        .split_whitespace()
        .map(|s| s.parse().ok().unwrap())
        .collect()
}

#[allow(dead_code)]
fn read_col<T: FromStr>(n: usize) -> Vec<T> {
    (0..n).map(|_| read()).collect()
}

#[allow(dead_code)]
fn read_mat<T: FromStr>(n: usize) -> Vec<Vec<T>> {
    (0..n).map(|_| read_row()).collect()
}

#[allow(dead_code)]
fn read_vec<R, F: FnMut() -> R>(n: usize, mut f: F) -> Vec<R> {
    (0..n).map(|_| f()).collect()
}

trait IterCopyExt<'a, T>: IntoIterator<Item = &'a T> + Sized
where
    T: 'a + Copy,
{
    fn citer(self) -> std::iter::Copied<Self::IntoIter> {
        self.into_iter().copied()
    }
}

impl<'a, T, I> IterCopyExt<'a, T> for I
where
    I: IntoIterator<Item = &'a T>,
    T: 'a + Copy,
{
}

fn dfs(
    edges: &Vec<Vec<(usize, usize)>>,
    dirs: &mut Vec<bool>,
    visited: &mut Vec<bool>,
    v: usize,
    c: &Vec<usize>,
) {
    if visited[v] {
        return;
    }
    visited[v] = true;

    let m = dirs.len();

    for &(u, i_edge) in &edges[v] {
        if c[v] != c[u] {
            continue;
        }

        dirs[i_edge % m] = (i_edge / m) != 0;

        dfs(edges, dirs, visited, u, c);
    }
}

fn main() {
    let (n, m) = read_tuple!(usize, usize);

    let ab = read_vec(m, || read_tuple!(usize, usize));

    let c = read_row::<usize>();

    let edges = ab
        .citer()
        .enumerate()
        .fold(vec![vec![]; n], |mut edges, (i, (a, b))| {
            edges[a - 1].push((b - 1, i));
            edges[b - 1].push((a - 1, m + i));
            edges
        });
    let mut dirs = vec![false; m];
    let mut visited = vec![false; n];
    for i in 0..n {
        dfs(&edges, &mut dirs, &mut visited, i, &c);
    }

    for i in 0..m {
        if c[ab[i].0 - 1] > c[ab[i].1 - 1] {
            println!("->");
        } else if c[ab[i].0 - 1] < c[ab[i].1 - 1] {
            println!("<-");
        } else if dirs[i] {
            println!("<-");
        } else {
            println!("->");
        }
    }
}
